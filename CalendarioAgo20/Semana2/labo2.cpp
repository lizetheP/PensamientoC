#include <stdio.h>
#include <stdlib.h>

void main()
{
   int x, res; // Declaración de variables

   printf("\n Da el valor de x : ");
   scanf("%i", &x);

   res = x*x;

   printf("\n El resultado es %i", res); // Imprime el valor de la variable res

   printf("\n\n\n");
   system("PAUSE"); // Instrucción que frena la ejecución del programa
}




